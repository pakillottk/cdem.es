/**
 * Lee las carpetas de public/producciones/ y genera un módulo TS estático.
 * JSON en SSR/Cloudflare a veces rompe deps del worker; un .ts evita eso.
 */
import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const produccionesDir = join(root, "public", "producciones");
const outFile = join(root, "src", "data", "galleryManifest.generated.ts");

/** @type {Record<string, string[]>} */
const manifest = {};

for (const entry of readdirSync(produccionesDir, { withFileTypes: true })) {
  if (!entry.isDirectory()) continue;
  const folder = entry.name;
  const folderPath = join(produccionesDir, folder);
  const files = readdirSync(folderPath, { withFileTypes: true })
    .filter((e) => e.isFile())
    .map((e) => e.name)
    .filter((name) => /\.(jpe?g|png|webp)$/i.test(name))
    .sort((a, b) =>
      a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }),
    );
  manifest[folder] = files;
}

const header =
  "/** @generated by scripts/build-gallery-manifest.mjs — ejecuta `npm run gallery:manifest` */\n\n";
const body = `export default ${JSON.stringify(manifest, null, 2)} as Record<string, string[]>;\n`;
writeFileSync(outFile, header + body, "utf8");
console.log(`Wrote ${outFile} (${Object.keys(manifest).length} folders).`);
