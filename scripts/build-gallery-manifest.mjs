/**
 * Lee las carpetas de public/producciones/ y genera un módulo TS estático.
 * Genera entradas separadas de thumb y full para las versiones WebP optimizadas.
 * JSON en SSR/Cloudflare a veces rompe deps del worker; un .ts evita eso.
 */
import { readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const produccionesDir = join(root, "public", "producciones");
const outFile = join(root, "src", "data", "galleryManifest.generated.ts");

/** @type {Record<string, { thumb: string; full: string }[]>} */
const manifest = {};

for (const entry of readdirSync(produccionesDir, { withFileTypes: true })) {
  if (!entry.isDirectory()) continue;
  const folder = entry.name;
  const folderPath = join(produccionesDir, folder);

  const allFiles = readdirSync(folderPath, { withFileTypes: true })
    .filter((e) => e.isFile())
    .map((e) => e.name);

  // Solo los archivos -thumb.webp; derivar el nombre -full.webp correspondiente
  const thumbFiles = allFiles
    .filter((name) => name.endsWith("-thumb.webp"))
    .sort((a, b) =>
      a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }),
    );

  manifest[folder] = thumbFiles.map((thumb) => ({
    thumb,
    full: thumb.replace(/-thumb\.webp$/, "-full.webp"),
  }));
}

const header =
  "/** @generated by scripts/build-gallery-manifest.mjs — ejecuta `npm run gallery:manifest` */\n\n";
const body = `export default ${JSON.stringify(manifest, null, 2)} as Record<string, { thumb: string; full: string }[]>;\n`;
writeFileSync(outFile, header + body, "utf8");
console.log(`Wrote ${outFile} (${Object.keys(manifest).length} folders).`);
